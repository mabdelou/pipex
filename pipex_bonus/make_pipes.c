#include "header.h"

int	make_pipes(t_node *node)
{
	int	a;

	a = -1;
	while (++a < (node->argc - 4) * 2)
		if (a % 2 == 0)
			if (pipe(&node->fds[a]) == -1)
				return (1);
	return (0);
}
